public void ReturnBook_Load()
{

int c;
        try {
            pst = con.prepareStatement("select l.id,m.name,b.bname,l.issuedate,l.returndate from lendbook l JOIN member m On l.memberid=m.id JOIN book b On b.id=l.bookid");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd =rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d =(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while (rs.next())
            {
            
            Vector v2 = new Vector();
            for (int i=1;i<=c;i++)
            {
            v2.add(rs.getString("id"));
            v2.add(rs.getString("mid"));
            v2.add(rs.getString("mname"));
            v2.add(rs.getString("bname"));
            v2.add(rs.getString("returndate"));
            v2.add(rs.getString("elp"));
            v2.add(rs.getString("fine"));
            
            }
            d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }

}



